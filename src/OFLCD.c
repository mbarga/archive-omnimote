void initLCD(){
	 int j;
	 int c;
	 int p;
	 int l;

	 IODIR0 = IODIR0 | ( LCD_A0 | LCD_CS1 | LCD_CS2 | LCD_RST | LCD_E | LCD_SDAT | LCD_SCLK );

	 OFLCDTimer = 0;
	 shiftLCDData(0x00);
	 IOCLR0 = LCD_RST;
	 for(j=0;j<100000;j++); // 80ns
	 IOSET0 = LCD_RST;
	 for(j=0;j<100000;j++); // 80ns
	 IOCLR0 = LCD_A0;
	 IOSET0 = LCD_CS1;
	 IOSET0 = LCD_CS2;

	 setComBoth(0xE2);
	 for(j=0;j<100000;j++); // 80ns
	 setComBoth(0xA4);

	 setComBoth (0xA9);

	 setComBoth (0xA0);	 //  Normal Mapping (0xA0, 0xA1)

	 setComBoth (0xEE);

	 setComBoth (0xC0);	 // Display Start Line (0xC0 - 	0xDF)
	 setComBoth (0xB8);	 // Display Data Memory Page (0xB8 - 	0xBB)

	 setComBoth (0xAF);

	 // Clear LCD
	for(c = 1; c <= 2; c++){
	 	for(p = 0; p < 4; p++){
			setCom(0xB8 + p,c);
			setCom(0x00,c);
	 		for(l = 0; l < 61; l++){
				writeData(0x00,c);
			}
		}
	}
}

void setComBoth(BYTE b){
	setCom(b,1);
	setCom(b,2);
}

void refreshLCD(BYTE wifi, enum OFMode mode){
	int l;
	drawLCDGraphic(getSignal(wifi, checkVertical()), 0);
	for(l = 1; l < 4; l++){
		drawLCDGraphic(getLetter(mode, checkVertical(), l), l);
	}
	OFLCDTimer = 0x00;
}

BYTE checkVertical() {
	if ((FIO4PIN&0x01000000)>>24) { 
		return 1; // pin high (vertical)
	} else {
		return 0;// pin low	 (horizontal)
	}
}

void shiftLCDData(BYTE byte){
	int i,j;
	BYTE localByte = byte;

	for (i=0; i<8; i++) {
		for(j=0;j<2;j++); //delayMs(1,1);
		if ((localByte & 0x01)==0x00) {
			IOCLR0 = LCD_SDAT;
		} else {
			IOSET0 = LCD_SDAT;
		}
		IOSET0 = LCD_SCLK;
		for(j=0;j<50;j++); // delayMs(1,1)
		IOCLR0 = LCD_SCLK;
		localByte = localByte>>1;
	}
}



void writeData(char data, BYTE chip) {
	int j;

	shiftLCDData(data);
	// write is always low
	IOSET0 = LCD_A0;
	if(chip == 1){
		IOCLR0 = LCD_CS1;
	}else{
		IOCLR0 = LCD_CS2;
	}
	IOSET0 = LCD_E;
	for(j=0;j<1000;j++);	// 80ns
	IOCLR0 = LCD_E;
	if(chip == 1){
		IOSET0 = LCD_CS1;
	}else{
		IOSET0 = LCD_CS2;
	}
	return;
}

void setCom(BYTE cmd, BYTE chip) {
	int j;

	shiftLCDData(cmd);
	IOCLR0 = LCD_A0;
	if(chip == 1){
		IOCLR0 = LCD_CS1;
	}else{
		IOCLR0 = LCD_CS2;
	}
	IOSET0 = LCD_E;
	for(j=0;j<50;j++);	// 80ns
	IOCLR0 = LCD_E;
	if(chip == 1){
		IOSET0 = LCD_CS1;
	}else{
		IOSET0 = LCD_CS2;
	}

	return;	
}

void drawLCDGraphic(GRAPHIC img, BYTE loc){
	int col; // used for loop index
	int col_off;
	int page;
	int chip;
	
	switch(loc){
	 	case 0:
			chip = 1;
			col_off = 0;
			break;
		case 1:
			chip = 1;
			col_off = 31;
			break;
		case 2:
			chip = 2;
			col_off = 0;
			break;
		case 3:
			chip = 	2;
			col_off = 31;
			break;
		default:
			return;
	}

	for(page = 0; page < 4; page++){
		setCom(col_off, chip);
		switch(page){
			case 0:
				setCom((BYTE)0xB8, chip);
				break;
			case 1:
				setCom((BYTE)0xB9, chip);
				break;
			case 2:
				setCom((BYTE)0xBA, chip);
				break;
			case 3:
				setCom((BYTE)0xBB, chip);
				break;
		}
		for(col = 0; col < 30; col++){
			writeData(OFGraphics[img][page][col], chip);
		}
	}
}			



