GRAPHIC getLetter(enum OFMode mode, BYTE rot, BYTE pos){
	 switch(mode){
			case HEY:
				switch(pos){
					case 1:
						return OFLetterH + rot;
					case 2:
						return OFLetterE + rot;
					case 3:
						return OFLetterY + rot;
				}
			break;
			case OK:
				switch(pos){
					case 1:
						return OFSignalNull;
					case 2:
						return OFLetterO + rot;
					case 3:
						return OFLetterK + rot;
				}
			break;
			case USB:
				switch(pos){
					case 1:
						return OFLetterU + rot;
					case 2:
						return OFLetterS + rot;
					case 3:
						return OFLetterB + rot;
				}
			break;
			case CON:
				switch(pos){
					case 1:
						return OFLetterC + rot;
					case 2:
						return OFLetterO + rot;
					case 3:
						return OFLetterN + rot;
				}
			break;
			case REC:
				switch(pos){
					case 1:
						return OFLetterR + rot;
					case 2:
						return OFLetterE + rot;
					case 3:
						return OFLetterC + rot;
				}
			break;
			case ERR:
				switch(pos){
					case 1:
						return OFLetterE + rot;
					case 2:
						return OFLetterR + rot;
					case 3:
						return OFLetterR + rot;
				}
			break;
			case CFG:
				switch(pos){
					case 1:
						return OFLetterC + rot;
					case 2:
						return OFLetterF + rot;
					case 3:
						return OFLetterG + rot;
				}
			break;
	 }
	 return (GRAPHIC)OFSignalNull;
}


